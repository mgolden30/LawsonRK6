function [a,b] = luther_RK6()
  %{
  Luther's RK6 "An Explicit Sixth-Order Runge-Kutta Formula"
  %}

  a = zeros(7,7);
  a(2,1:1) = [1];
  a(3,1:2) = [3/8, 1/8];
  a(4,1:3) = [8/27, 2/27, 8/27];
  s = sqrt(21);
  a(5,1:4) = [3*(3*s-7), -8*(7-s), 48*(7-s), -3*(21-s)]/392;
  a(6,1:5) = [-5*(231 + 51*s), -40*(7+s), -320*s, 3*(21 + 121*s), 392*(6+s)]/1960;
  a(7,1:6) = [15*(22+7*s), 120, 40*(7*s - 5), -63*(3*s-2), -14*(49+9*s), 70*(7-s)]/180;

  b = [9, 0, 64, 0, 49, 49, 9]/180;
end